let robot = new Robot(18, 83, 0, 0.5, 0.01, 0.85, 0.85, 10, 0.1);

let discs = discsCords.map(([x,y]) => new Disc(x,y, 16.5));

let goals = [new Goal("red"), new Goal("blue")];

let field = new Field();

function setup() {
  createCanvas(365, 365);
  background(255);
}

function draw() {
  frameRate(60);

  field.draw();

  discs.map((disc) => {
    disc.draw();
    disc.checkRobotCollision(robot.corners);
    disc.checkDiscsCollision();
  });

  robot.handleInput();
  
  goals.map((goal) => goal.draw());
};
